inscription = "Devs-Mentoring"
small_inscription = inscription.lower()
print(small_inscription)
# tylko małe litery

big_inscription = inscription.upper()
print(big_inscription)
# tylko duże litery

first_letter_big = inscription.capitalize()
print(first_letter_big)
# pierwsza duża

first_letters_big = inscription.title()
print(first_letters_big)
# pierwsze duże

sth_more = inscription.swapcase()
print(sth_more)
# że pierwsza litera jest mała a reszta duża?