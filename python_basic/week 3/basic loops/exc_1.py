# for loop
print("this program will show you numbers from 0 to y. ")
y = int(input("please enter y for for loop: "))
for i in range(0, y):
    print(i + 1)


# while loop
y = int(input("please enter y for while loop: "))
numerator = 0
while numerator < y:
    print(numerator + 1)
    numerator += 1
