string1 = "   przykład"
left_stripped_string = string1.lstrip()
print(left_stripped_string)
#usunie od lewej

string2 = "przykład   "
right_stripped_string = string2.rstrip()
print(right_stripped_string)
#usunie od prawej

string3 = "  przykład   "
both_stripped_string = string3.strip()
print(both_stripped_string)
#usunie z obu stron